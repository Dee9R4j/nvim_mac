local M = {}

local font_presets = {
  "JetBrainsMono Nerd Font",
  "FiraCode Nerd Font",
  "Hack Nerd Font",
  "Iosevka Nerd Font",
  "MesloLGS Nerd Font",
}

local state_path = vim.fn.stdpath("state") .. "/control_center_state.json"
local plugins_state_path = vim.fn.stdpath("data") .. "/control_center_plugins.json"
local user_plugins_path = vim.fn.stdpath("config") .. "/lua/plugins/control_center_user.lua"

local default_state = {
  theme = nil,
  font_family = "JetBrainsMono Nerd Font",
  font_size = 14,
  neovide_scale = 1.0,
  options = {
    relativenumber = true,
    wrap = true,
    spell = false,
    virtual_text = false,
    tabstop = 2,
    shiftwidth = 2,
    expandtab = true,
  },
}

local state = vim.deepcopy(default_state)

local function notify(msg, level)
  vim.notify(msg, level or vim.log.levels.INFO, { title = "Control Center" })
end

local function read_json(path, fallback)
  if vim.fn.filereadable(path) ~= 1 then return fallback end

  local ok_read, lines = pcall(vim.fn.readfile, path)
  if not ok_read or not lines then return fallback end
  if not lines or vim.tbl_isempty(lines) then return fallback end

  local ok, decoded = pcall(vim.json.decode, table.concat(lines, "\n"))
  if not ok or type(decoded) ~= "table" then return fallback end
  return decoded
end

local function write_json(path, payload)
  local dir = vim.fn.fnamemodify(path, ":h")
  vim.fn.mkdir(dir, "p")
  local encoded = vim.json.encode(payload)
  vim.fn.writefile(vim.split(encoded, "\n"), path)
end

local function save_state()
  write_json(state_path, state)
end

local function ensure_user_plugins_file(repos)
  vim.fn.mkdir(vim.fn.fnamemodify(user_plugins_path, ":h"), "p")
  local lines = {
    "-- Auto-generated by Control Center",
    "-- Edit from Control Center > Tools and Package Managers",
    "return {",
  }

  for _, repo in ipairs(repos) do
    table.insert(lines, string.format("  { %q },", repo))
  end

  table.insert(lines, "}")
  vim.fn.writefile(lines, user_plugins_path)
end

local function get_user_plugins()
  local repos = read_json(plugins_state_path, {})
  if type(repos) ~= "table" then repos = {} end
  return repos
end

local function save_user_plugins(repos)
  write_json(plugins_state_path, repos)
  ensure_user_plugins_file(repos)
end

local function select_menu(prompt, items)
  vim.ui.select(items, {
    prompt = prompt,
    format_item = function(item)
      return item.label
    end,
  }, function(choice)
    if choice and choice.action then choice.action() end
  end)
end

local function ask_input(prompt, default, on_confirm)
  vim.ui.input({ prompt = prompt, default = default or "" }, function(value)
    if value and value ~= "" then on_confirm(value) end
  end)
end

local function current_font_size()
  local guifont = vim.o.guifont or ""
  return tonumber(guifont:match(":h(%d+)")) or 14
end

local function set_font_size(size)
  local target = math.max(8, math.min(42, size))
  local guifont = vim.o.guifont or ""

  if guifont == "" then
    vim.o.guifont = "JetBrainsMono Nerd Font:h" .. target
  elseif guifont:find(":h%d+") then
    vim.o.guifont = guifont:gsub(":h%d+", ":h" .. target, 1)
  else
    vim.o.guifont = guifont .. ":h" .. target
  end

  state.font_size = target
  save_state()

  notify("Text size set to " .. target)
end

local function current_font_family()
  local guifont = vim.o.guifont or ""
  if guifont == "" then return "JetBrainsMono Nerd Font" end
  return guifont:match("^(.-):") or guifont
end

local function set_font_family(family)
  local size = current_font_size()
  vim.o.guifont = family .. ":h" .. size
  state.font_family = family
  state.font_size = size
  save_state()
  notify("Font set to " .. family)
end

local function adjust_text_size(delta)
  if vim.g.neovide then
    local scale = tonumber(vim.g.neovide_scale_factor) or 1.0
    local next_scale = math.max(0.6, math.min(2.2, scale + (delta * 0.1)))
    vim.g.neovide_scale_factor = next_scale
    state.neovide_scale = next_scale
    save_state()
    notify("Text scale set to " .. string.format("%.1fx", next_scale))
    return
  end

  set_font_size(current_font_size() + delta)
end

local function reset_text_size()
  if vim.g.neovide then
    vim.g.neovide_scale_factor = 1.0
    state.neovide_scale = 1.0
    save_state()
  end
  set_font_size(14)
end

local function toggle_diagnostic_text()
  local cfg = vim.diagnostic.config()
  local next_state = not cfg.virtual_text
  vim.diagnostic.config({ virtual_text = next_state })
  state.options.virtual_text = next_state
  save_state()
  notify("Diagnostics text " .. (next_state and "enabled" or "disabled"))
end

local function toggle_inlay_hints()
  if not vim.lsp.inlay_hint then
    notify("Inlay hints not supported by this Neovim version", vim.log.levels.WARN)
    return
  end

  local enabled = false
  pcall(function()
    enabled = vim.lsp.inlay_hint.is_enabled({ bufnr = 0 })
  end)

  pcall(function()
    vim.lsp.inlay_hint.enable(not enabled, { bufnr = 0 })
  end)

  notify("Inlay hints " .. ((not enabled) and "enabled" or "disabled"))
end

local function apply_theme(theme)
  local ok = pcall(vim.cmd, "colorscheme " .. theme)
  if not ok then
    notify("Could not apply theme: " .. theme, vim.log.levels.WARN)
    return
  end

  state.theme = theme
  save_state()
  notify("Theme set to " .. theme)
end

local function set_tab_size(value)
  local n = tonumber(value)
  if not n or n < 1 or n > 12 then
    notify("Tab size must be between 1 and 12", vim.log.levels.WARN)
    return
  end

  vim.o.tabstop = n
  vim.o.shiftwidth = n
  state.options.tabstop = n
  state.options.shiftwidth = n
  save_state()
  notify("Tab size set to " .. n)
end

local function set_expandtab(value)
  vim.o.expandtab = value
  state.options.expandtab = value
  save_state()
  notify(value and "Spaces enabled" or "Tabs enabled")
end

