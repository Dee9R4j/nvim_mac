local function apply_state()
  state = vim.tbl_deep_extend("force", vim.deepcopy(default_state), read_json(state_path, {}))

  vim.o.relativenumber = state.options.relativenumber
  vim.o.wrap = state.options.wrap
  vim.o.spell = state.options.spell
  vim.o.tabstop = state.options.tabstop
  vim.o.shiftwidth = state.options.shiftwidth
  vim.o.expandtab = state.options.expandtab

  vim.diagnostic.config({ virtual_text = state.options.virtual_text })

  if state.font_family and state.font_size then
    vim.o.guifont = string.format("%s:h%d", state.font_family, state.font_size)
  end

  if vim.g.neovide then
    vim.g.neovide_scale_factor = state.neovide_scale or 1.0
  end

  if state.theme and state.theme ~= "" then
    pcall(vim.cmd, "colorscheme " .. state.theme)
  end

  ensure_user_plugins_file(get_user_plugins())
end

function M.setup()
  if vim.g.control_center_setup_done then return end
  vim.g.control_center_setup_done = true

  apply_state()

  vim.api.nvim_create_user_command("ControlCenter", function()
    M.open()
  end, { desc = "Open GUI Control Center" })
end

return M
