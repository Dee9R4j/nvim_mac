local function open_mason_packages_menu()
  select_menu("Mason Package Actions", {
    {
      label = "Install Mason Package",
      action = function()
        ask_input("Package name (e.g. lua-language-server): ", "", function(name)
          vim.cmd("MasonInstall " .. name)
        end)
      end,
    },
    {
      label = "Uninstall Mason Package",
      action = function()
        local ok, registry = pcall(require, "mason-registry")
        if not ok then
          notify("Mason registry not available", vim.log.levels.WARN)
          return
        end

        local installed = registry.get_installed_package_names()
        if #installed == 0 then
          notify("No Mason packages installed")
          return
        end

        vim.ui.select(installed, { prompt = "Uninstall package" }, function(choice)
          if choice then vim.cmd("MasonUninstall " .. choice) end
        end)
      end,
    },
  })
end

local function open_plugins_menu()
  select_menu("Plugin Actions", {
    { label = "Open Plugin Manager", action = function() vim.cmd("Lazy") end },
    {
      label = "Install Plugin by Repo",
      action = function()
        ask_input("GitHub repo (owner/name): ", "", function(repo)
          if not repo:match("^[%w%._-]+/[%w%._-]+$") then
            notify("Invalid repo format. Use owner/name", vim.log.levels.WARN)
            return
          end

          local repos = get_user_plugins()
          if vim.tbl_contains(repos, repo) then
            notify(repo .. " is already in your managed plugin list")
            return
          end

          table.insert(repos, repo)
          table.sort(repos)
          save_user_plugins(repos)
          vim.cmd("Lazy sync")
          notify("Added plugin " .. repo .. " and started sync")
        end)
      end,
    },
    {
      label = "Remove Managed Plugin",
      action = function()
        local repos = get_user_plugins()
        if #repos == 0 then
          notify("No Control Center managed plugins to remove")
          return
        end

        vim.ui.select(repos, { prompt = "Remove plugin" }, function(choice)
          if not choice then return end

          local next_repos = {}
          for _, repo in ipairs(repos) do
            if repo ~= choice then table.insert(next_repos, repo) end
          end

          save_user_plugins(next_repos)
          vim.cmd("Lazy sync")
          notify("Removed plugin " .. choice .. " and started sync")
        end)
      end,
    },
  })
end

local function open_appearance_menu()
  select_menu("Appearance", {
    {
      label = "Theme Picker",
      action = function()
        local schemes = vim.fn.getcompletion("", "color")
        table.sort(schemes)
        vim.ui.select(schemes, { prompt = "Choose theme" }, function(choice)
          if choice then apply_theme(choice) end
        end)
      end,
    },
    {
      label = "Theme Toggle (light/dark)",
      action = function()
        local ok, base46 = pcall(require, "base46")
        if ok and base46.toggle_theme then
          base46.toggle_theme()
          state.theme = vim.g.colors_name
          save_state()
        else
          vim.cmd("Telescope themes")
        end
      end,
    },
    {
      label = "Choose Font Family",
      action = function()
        vim.ui.select(font_presets, {
          prompt = "Choose font family",
          format_item = function(item)
            local marker = (item == current_font_family()) and " *" or ""
            return item .. marker
          end,
        }, function(choice)
          if choice then set_font_family(choice) end
        end)
      end,
    },
    { label = "Text Size +", action = function() adjust_text_size(1) end },
    { label = "Text Size -", action = function() adjust_text_size(-1) end },
    { label = "Reset Text Size", action = reset_text_size },
    {
      label = "Set Neovide Opacity",
      action = function()
        ask_input("Opacity (0.3 - 1.0): ", tostring(vim.g.neovide_opacity or 0.9), function(value)
          local n = tonumber(value)
          if not n or n < 0.3 or n > 1.0 then
            notify("Opacity must be between 0.3 and 1.0", vim.log.levels.WARN)
            return
          end
          vim.g.neovide_opacity = n
          notify("Opacity set to " .. n)
        end)
      end,
    },
  })
end

